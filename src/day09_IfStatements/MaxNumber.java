package day09_IfStatements;

public class MaxNumber {

    public static void main(String[] args) {

        int num1 = 23, num2 = 12;

        if (num1 > num2){
            System.out.println(num1 + " is the max number.");

        } else {
            System.out.println(num2 + " is the max number.");
        }

    }
}
