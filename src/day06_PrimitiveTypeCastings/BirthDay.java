package day06_PrimitiveTypeCastings;



public class BirthDay {

    public static void main(String[] args) {

        String name = "John";
        int birthday = 25;
        String  birthMonth = "April";
        int birthYear = 1995;

        System.out.println(name + " was born on " + birthMonth+
                "/"+ birthday+ "/" + birthYear + "." );

    }
}
