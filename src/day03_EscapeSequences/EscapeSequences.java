package day03_EscapeSequences;


/*  escape sequnces: MUST be given with double quote
     \n : starts a new line
     \t : (tab) paragraph space
     \\ : prints single back slash
     \" : double quote




 */

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \\\\Python \nC#");

    }
}
